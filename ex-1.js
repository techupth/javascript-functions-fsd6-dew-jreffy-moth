// Start coding here
function add(a, b) {
  const addNumber = a + b;
  return addNumber;
}

function subtract(a, b) {
  const subtractNumber = a - b;
  return subtractNumber;
}

function multiply(a, b) {
  const multiplyNumber = a * b;
  return multiplyNumber;
}

function divide(a, b) {
  const divideNumber = a / b;
  return divideNumber;
}

const addResult = add(10, 5);
const subtractResult = subtract(10, 5);
const multiplyResult = multiply(10, 5);
const divideResult = divide(10, 5);
console.log(addResult, subtractResult, multiplyResult, divideResult);
