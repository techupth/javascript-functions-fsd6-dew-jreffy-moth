// Start coding here
const add = function (a, b) {
  const addNumber = a + b;
  return addNumber;
};

const subtract = function (a, b) {
  const addNumber = a - b;
  return addNumber;
};

const multiply = function (a, b) {
  const addNumber = a * b;
  return addNumber;
};

const divide = function (a, b) {
  const addNumber = a / b;
  return addNumber;
};

const addResult = add(10, 5);
const subtractResult = subtract(10, 5);
const multiplyResult = multiply(10, 5);
const divideResult = divide(10, 5);
console.log(addResult, subtractResult, multiplyResult, divideResult);
